/* ЁЯФз Helper functions for all pages */
function detectLang() {
  const stored = JSON.parse(localStorage.getItem("daylifeProfile") || "{}").lang;
  if (stored && LANGS[stored]) return stored;
  const nav = (navigator.language || "en").slice(0, 2);
  return LANGS[nav] ? nav : "en";
}

function getText(key, lang) {
  const l = lang || detectLang();
  return LANGS[l][key] || LANGS["en"][key] || "";
}
